document.addEventListener('DOMContentLoaded', function() {
    // Tab functionality
    const appTabBtns = document.querySelectorAll('.app-tab-btn');
    const appTabContents = document.querySelectorAll('.app-tab-content');
    
    appTabBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            const tabId = btn.getAttribute('data-tab');
            
            appTabBtns.forEach(b => b.classList.remove('active'));
            appTabContents.forEach(t => t.classList.remove('active'));
            
            btn.classList.add('active');
            document.getElementById(tabId).classList.add('active');
        });
    });
    
    // ROI Calculator
    const calculateBtn = document.querySelector('.calculate-btn');
    if (calculateBtn) {
        calculateBtn.addEventListener('click', function() {
            const revenue = parseFloat(document.getElementById('revenue').value) || 500000;
            const collectionRate = parseFloat(document.getElementById('collection-rate').value) || 85;
            const daysAR = parseFloat(document.getElementById('days-ar').value) || 45;
            const staff = parseFloat(document.getElementById('staff').value) || 3;
            
            // Calculate improvements
            const improvedCollectionRate = Math.min(collectionRate + 12, 98);
            const improvedDaysAR = Math.max(daysAR - 20, 25);
            const staffReduction = Math.max(staff * 0.6, 1);
            
            // Calculate benefits
            const annualRevenue = revenue * 12;
            const increasedCollections = (annualRevenue * (improvedCollectionRate - collectionRate)) / 100;
            const fasterCash = (annualRevenue * (daysAR - improvedDaysAR)) / 365 * 0.05;
            const staffSavings = (staff - staffReduction) * 65000;
            const totalBenefit = increasedCollections + fasterCash + staffSavings;
            const roi = ((totalBenefit - 185000) / 185000) * 100;
            
            // Update display
            document.getElementById('increased-collections').textContent = '$' + Math.round(increasedCollections).toLocaleString();
            document.getElementById('faster-cash').textContent = '$' + Math.round(fasterCash).toLocaleString();
            document.getElementById('staff-savings').textContent = '$' + Math.round(staffSavings).toLocaleString();
            document.getElementById('total-benefit').textContent = '$' + Math.round(totalBenefit).toLocaleString();
            document.getElementById('roi-percentage').textContent = Math.round(roi) + '%';
        });
    }
});
