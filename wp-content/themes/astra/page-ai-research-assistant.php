/* JavaScript for interactive functionality */
document.addEventListener('DOMContentLoaded', function() {
    // Tab functionality
    const tabBtns = document.querySelectorAll('.tab-btn');
    const tabContents = document.querySelectorAll('.tab-content');
    
    tabBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            const tabId = btn.getAttribute('data-tab');
            
            tabBtns.forEach(b => b.classList.remove('active'));
            tabContents.forEach(t => t.classList.remove('active'));
            
            btn.classList.add('active');
            document.getElementById(tabId).classList.add('active');
        });
    });
    
    // ROI Calculator
    const calculateROI = () => {
        const hours = parseFloat(document.getElementById('research-hours').value) || 40;
        const rate = parseFloat(document.getElementById('hourly-rate').value) || 75;
        const researchers = parseFloat(document.getElementById('num-researchers').value) || 3;
        const projects = parseFloat(document.getElementById('projects-year').value) || 12;
        
        const monthlyHours = hours * researchers;
        const annualHours = monthlyHours * 12;
        const timeSaved = Math.round(annualHours * 0.89); // 89% efficiency gain
        const costSavings = Math.round(timeSaved * rate);
        const outputIncrease = Math.round(234); // Fixed 234% increase
        const serviceCost = 15000; // Approximate annual cost
        const roi = Math.round(((costSavings - serviceCost) / serviceCost) * 100);
        
        document.getElementById('time-saved').textContent = timeSaved.toLocaleString();
        document.getElementById('cost-savings').textContent = '$' + costSavings.toLocaleString();
        document.getElementById('output-increase').textContent = outputIncrease + '%';
        document.getElementById('annual-roi').textContent = roi + '%';
    };
    
    // Add event listeners to calculator inputs
    const calculatorInputs = document.querySelectorAll('.calculator-inputs input');
    calculatorInputs.forEach(input => {
        input.addEventListener('input', calculateROI);
    });
    
    // Initial calculation
    calculateROI();
});
